/**
 * Save PDF to Media Library
 */
function satc_save_pdf_to_media() {
    check_ajax_referer('satc_pdf_nonce', 'nonce');

    if (empty($_POST['pdf_base64'])) {
        wp_send_json_error(array('message' => 'No PDF data received.'));
    }

    $pdf_base64 = wp_unslash($_POST['pdf_base64']);
    $file_name  = !empty($_POST['file_name']) ? sanitize_file_name(wp_unslash($_POST['file_name'])) : 'sa-tax-summary.pdf';

    if (strpos($pdf_base64, 'base64,') !== false) {
        $pdf_base64 = substr($pdf_base64, strpos($pdf_base64, 'base64,') + 7);
    }

    $pdf_binary = base64_decode($pdf_base64);

    if ($pdf_binary === false) {
        wp_send_json_error(array('message' => 'PDF decoding failed.'));
    }

    $upload = wp_upload_bits($file_name, null, $pdf_binary);

    if (!empty($upload['error'])) {
        wp_send_json_error(array('message' => $upload['error']));
    }

    $filetype = wp_check_filetype($upload['file'], null);

    $attachment = array(
        'post_mime_type' => $filetype['type'] ? $filetype['type'] : 'application/pdf',
        'post_title'     => sanitize_text_field(pathinfo($file_name, PATHINFO_FILENAME)),
        'post_content'   => '',
        'post_status'    => 'inherit',
    );

    $attachment_id = wp_insert_attachment($attachment, $upload['file']);

    if (is_wp_error($attachment_id) || !$attachment_id) {
        wp_send_json_error(array('message' => 'Attachment could not be created.'));
    }

    update_attached_file($attachment_id, $upload['file']);

    wp_send_json_success(array(
        'message'       => 'PDF saved successfully.',
        'attachment_id' => $attachment_id,
        'url'           => $upload['url'],
    ));
}
add_action('wp_ajax_satc_save_pdf_to_media', 'satc_save_pdf_to_media');
add_action('wp_ajax_nopriv_satc_save_pdf_to_media', 'satc_save_pdf_to_media');

/**
 * Enqueue assets
 */
function satc_enqueue_assets() {
    wp_enqueue_style(
        'satc-style',
        plugin_dir_url(__FILE__) . 'assets/css/calculator.css',
        array(),
        '2.2'
    );

    wp_enqueue_script(
        'satc-jspdf',
        plugin_dir_url(__FILE__) . 'assets/vendor/jspdf/jspdf.umd.min.js',
        array(),
        '2.5.1',
        true
    );

    wp_enqueue_script(
        'satc-script',
        plugin_dir_url(__FILE__) . 'assets/js/calculator.js',
        array('satc-jspdf'),
        '2.2',
        true
    );

    wp_localize_script(
        'satc-script',
        'satcSettings',
        satc_get_settings()
    );

    wp_localize_script(
        'satc-script',
        'satcAjax',
        array(
            'ajax_url' => admin_url('admin-ajax.php'),
            'nonce'    => wp_create_nonce('satc_pdf_nonce'),
        )
    );
}
add_action('wp_enqueue_scripts', 'satc_enqueue_assets');

