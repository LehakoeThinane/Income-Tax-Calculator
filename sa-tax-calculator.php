/**
 * Get merged settings
 */
function satc_get_settings() {
    $saved = get_option('satc_settings', array());
    return wp_parse_args($saved, satc_get_default_settings());
}

/**
 * Sanitize settings
 */
function satc_sanitize_settings($input) {
    $defaults = satc_get_default_settings();
    $output = array();

    $color_fields = array(
        'brand_primary',
        'brand_primary_dark',
        'brand_accent',
        'brand_accent_dark',
        'brand_highlight',
        'brand_danger',
    );

    foreach ($color_fields as $field) {
        $output[$field] = isset($input[$field]) ? sanitize_hex_color($input[$field]) : $defaults[$field];

        if (empty($output[$field])) {
            $output[$field] = $defaults[$field];
        }
    }

    $number_fields = array(
        'uif_rate',
        'uif_monthly_cap',
        'threshold_under65',
        'threshold_65to74',
        'threshold_75plus',
        'rebate_under65',
        'rebate_65to74',
        'rebate_75plus',
        'medical_credit_first',
        'medical_credit_second',
        'medical_credit_additional',
    );

    foreach ($number_fields as $field) {
        $output[$field] = isset($input[$field]) ? (string) floatval($input[$field]) : $defaults[$field];
    }

    $text_fields = array(
        'pdf_company_name',
        'pdf_footer_text',
        'pdf_disclaimer_title',
    );

    foreach ($text_fields as $field) {
        $output[$field] = isset($input[$field]) ? sanitize_text_field($input[$field]) : $defaults[$field];
    }

    $output['pdf_logo_url'] = isset($input['pdf_logo_url']) ? esc_url_raw($input['pdf_logo_url']) : $defaults['pdf_logo_url'];

    $output['pdf_disclaimer_body'] = isset($input['pdf_disclaimer_body'])
        ? wp_kses_post($input['pdf_disclaimer_body'])
        : $defaults['pdf_disclaimer_body'];

    return $output;
}

/**
 * Register settings
 */
function satc_register_settings() {
    register_setting(
        'satc_settings_group',
        'satc_settings',
        array(
            'type'              => 'array',
            'sanitize_callback' => 'satc_sanitize_settings',
            'default'           => satc_get_default_settings(),
        )
    );
}
add_action('admin_init', 'satc_register_settings');

