/**
 * Render standard input field
 */
function satc_render_input_field($name, $label, $type = 'text', $step = '0.01') {
    $settings = satc_get_settings();
    $value = isset($settings[$name]) ? $settings[$name] : '';

    echo '<tr>';
    echo '<th scope="row"><label for="' . esc_attr($name) . '">' . esc_html($label) . '</label></th>';
    echo '<td>';
    echo '<input type="' . esc_attr($type) . '" id="' . esc_attr($name) . '" name="satc_settings[' . esc_attr($name) . ']" value="' . esc_attr($value) . '" class="regular-text" ' . ($type === 'number' ? 'step="' . esc_attr($step) . '"' : '') . ' />';
    echo '</td>';
    echo '</tr>';
}

